export const flag  = (country) => {
  if (country === 'US') {
    return '🇺🇸'
  } else if (country === 'FR') {
    return '🇫🇷'
  } else if (country === 'SE') {
    return '🇸🇪'
  } else if (country === 'JP') {
    return '🇯🇵'
  } else if (country === 'MA') {
    return '🇲🇦'
  } else {
    return '🏴‍☠️'
  }
}
